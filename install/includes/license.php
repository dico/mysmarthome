<p>
		mySmartHome (MSH) is a smarthome web-application-framework (msh-core) written in PHP/MySQL.
		MSH supports bindings to third-party API's, custom themes and modules.
	</p>

	<p>The MSH framework is licensed under:</p>

	<div style="text-align:center; font-size:16px; padding: 10px 0px;">
		<a href="https://creativecommons.org/licenses/by-nc/3.0/" target="_blank">Creative Commons Attribution-NonCommercial 3.0 Unported License (CC BY-NC 3.0)</a>
	</div>


	<p>When installing MSH, you are approving the MSH license agreement and also the agreement for the included packages.</p>
	<p>
		Note that all third-party packages (bindings, themes, modules...) can have their own license agreements.
		You also need to be aware that all packages installed on MSH will have full permission to the whole MSH,
		meaning a third-party could control your home.
	</p>


